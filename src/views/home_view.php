				<h2>W Polsce</h2>
				<p>
					Rozwój fantastyki w Polsce był stosunkowo nierównomierny. Gdy za granicą fantasy cieszyło się już dużą popularnością, w naszym kraju praktycznie nie istniało aż do lat 80. XX wieku. Polskie utwory science-fiction zostały rozpropagowane między innymi dzięki sukcesowi Stanisława Lema, pisarza literatury science-fiction, w latach 50. XX wieku. Drugim istotnym twórcą okazał się Janusz A. Zajdel, uznawany za ojca polskiej fantastyki socjologicznej.
					<br />
					W 1982 roku powstał miesięcznik <i>Fantastyka</i>, którego celem było propagowanie literatury z tego gatunku. Czasopismo istnieje do dziś, od 1990 roku pod zmienioną nazwą <i>Nowa Fantastyka</i>. To na jego łamach pojawiło się jedno z dwóch pierwszych polskich opowiadań fantasy pod tytułem <i>Mag</i>, autorstwa Jacka Piekary. W 1986 roku na łamach magazynu ukazało się również opowiadanie <i>Wiedźmin</i> Andrzeja Sapkowskiego.
